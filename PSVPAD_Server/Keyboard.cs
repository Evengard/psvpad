        private void keyboard_Click(object sender, EventArgs e)
        {
            Button button = (Button)sender;
            string str1 = "";
            string str2 = "";
            bool flag = false;
            if (this.ctrlDown)
            {
                str1 += "^";
                str2 += "Ctrl + ";
                flag = true;
            }
            if (this.altDown)
            {
                str1 += "%";
                str2 += "Alt + ";
                flag = true;
            }
            if (this.shiftDown)
            {
                str1 += "+";
                str2 += "Shift + ";
                flag = true;
            }
            string lower = button.Tag.ToString().ToLower();
            string keys = !flag ? str1 + "{" + lower + "}" : str1 + "(" + lower + ")";
            Program.PSVServer.removeBind(PSVServer.lastButtonPressed);
            Program.PSVServer.addBind(PSVServer.lastButtonPressed, keys);
            if (PSVServer.buttonDict.ContainsKey(PSVServer.lastButtonPressed))
                PSVServer.buttonDict[PSVServer.lastButtonPressed].Text = str2 + button.Text;
            if (lower != "ctrl" && lower != "shift" && lower != "alt")
            {
                this.Close();
            }
            else
            {
                string str3 = str2.Remove(str2.Length - 2);
                PSVServer.buttonDict[PSVServer.lastButtonPressed].Text = str3;
            }
        }

        private void mouse_Click(object sender, EventArgs e)
        {
            Button button = (Button)sender;
            string keys = button.Tag.ToString();
            Program.PSVServer.removeBind(PSVServer.lastButtonPressed);
            Program.PSVServer.addMouseBind(PSVServer.lastButtonPressed, keys);
            if (PSVServer.buttonDict.ContainsKey(PSVServer.lastButtonPressed))
                PSVServer.buttonDict[PSVServer.lastButtonPressed].Text = button.Text;
            this.Close();
